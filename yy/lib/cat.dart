class Book {
  String cat_id;
  String price;
  String description;
  String location;
  String publisher;

  Book(
    String cat_id,
    String price,
    String description,
    String location,
    String publisher,
  ) {
    this.cat_id = cat_id;
    this.price = price;
    this.description = description;
    this.location = location;
    this.publisher = publisher;
  }
}
